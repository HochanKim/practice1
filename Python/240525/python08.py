# 어떤 수를 %로 나눠서 배수인지 아닌지 판별
print(100 % 5 == 0)
print(120 % 5 == 0)
print(14 % 5 == 0)  # 연산결과 : 4
print()

# 어떤 수가 6의 배수인지 아닌지 확인해보자
print(24 % 6 == 0)
print(124 % 6 == 0)
print(2234 % 6 == 0)
print(2346 % 6 == 0)
print()

# 어떤 수가 홀수인지 짝수인지 => %2의 결과가 1이면 홀수, 0이면 짝수
print(7 % 2 == 0)   # 홀수 (false)
print(10 % 2 == 0)  # 짝수 (true)
print()

# 약수로 나누어 떨어지는 수 -> ex. 8
