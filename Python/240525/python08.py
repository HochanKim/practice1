# 잔돈 구하기
# 5,700원을 500원짜리로 거슬러주면 500원 동전 몇 개 남은돈은 얼마인가?
print("500원 동전을 제외한 남은 돈 :", 5700 % 500)
print("500원 동전의 개수 :", 5700 // 500)
print()

# 시, 분, 초 변환
# 10,000초는 몇 시간, 몇 분, 몇 초인가?
print("시간 (60분 = 3600초) :", 10000 // 3600)  # 2시간
print("시간 구하고 분단위 (1분 = 60초) :", 10000 % 3600 // 60)   # 46분
print("분단위 구하고 초단위 :", 10000 % 3600 % 60)    # 40초
print()

