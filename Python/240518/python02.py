# 파이썬에서 상수 표기법
# 상수 : 변하지 않는 수 / 변수 : 변하는 수 / 함수 : 복잡한 수식을 기호로 표시한 것

# 정수 int
print(7)    # 정수(int) 출력 예시
print(-5)
print(0)
print(type(65)) # type 해당 데이터의 자료형 출력
print()

# 실수 float
print(2.5)  # 실수(float) 출력 예시
print(.23)
print(-3.14)
print(type(-1.23))
print()

# 문자열 str => '' or "" 로 묶으면 문자열이 된다.
print('안녕') 
print("Hello World")
print(type("김호찬")) 
print()

# 불리언 bool => "true"와 "false"를 분별
print(True)
print(type(False))
print()


